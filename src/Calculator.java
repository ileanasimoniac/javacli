public class Calculator {

    public int sum(int firstNumber, int secondNumber) {
        int result = firstNumber + secondNumber; // face operatia de adunare
        return result; // returneaza valoarea adunata
    }

    public int diff(int firstNumber, int secondNumber) {
        return firstNumber - secondNumber;
    }

    public float sum(float first, float second) {
        return first + second;
    }

    public float multiply(float first, float second) {
        return first * second;
    }

    public float divide(float first, float second) {
        return first / second;
    }
    // aici vor fi resul operatiilor matematice
}
